function EquipBase.Get(id)
    return EquipBase.data[id]
end

function EquipBase.GetBySlot(slot_type)
    local result = {}
    for id, eq in pairs(EquipBase.data) do
        if eq.slot_type == slot_type then
            result[#result + 1] = eq
        end
    end
    return result
end

function EquipBase.GetByQuality(quality)
    local result = {}
    for id, eq in pairs(EquipBase.data) do
        if eq.quality == quality then
            result[#result + 1] = eq
        end
    end
    return result
end

function EquipBase.GetByLevel(level)
    local result = {}
    for id, eq in pairs(EquipBase.data) do
        if eq.level_req > 0 and eq.level_req <= level then
            result[#result + 1] = eq
        end
    end
    return result
end

function EquipBase.CanEquip(id, player_level)
    local eq = EquipBase.data[id]
    if not eq then return false end
    if eq.level_req == 0 then return true end
    return player_level >= eq.level_req
end

function EquipBase.GetSellPrice(id)
    local eq = EquipBase.data[id]
    if not eq then return 0 end
    return math.min(eq.sell_price, 999999)
end

function EquipBase.GetBuyPrice(id)
    local eq = EquipBase.data[id]
    if not eq then return 0 end
    return math.min(eq.buy_price, 999999)
end

function EquipBase.GetAtkRange(id)
    local eq = EquipBase.data[id]
    if not eq then return 0, 0 end
    return math.min(eq.atk_min, 999999), math.min(eq.atk_max, 999999)
end

function EquipBase.GetDefense(id)
    local eq = EquipBase.data[id]
    if not eq then return 0 end
    return math.min(eq.defense, 999999)
end

function EquipBase.HasEffect(id)
    local eq = EquipBase.data[id]
    if not eq then return false end
    return eq.effect_icon ~= nil and eq.effect_icon ~= ""
end

function EquipBase.GetAllIDs()
    local ids = {}
    for id in pairs(EquipBase.data) do
        ids[#ids + 1] = id
    end
    table.sort(ids)
    return ids
end

function EquipBase.Count()
    local n = 0
    for _ in pairs(EquipBase.data) do n = n + 1 end
    return n
end

return EquipBase